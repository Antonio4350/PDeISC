function suma(num1,num2){//creo la funcion suma con dos parametros
    return(num1+num2);//retorno su resultado
}

function resta(num1,num2){//creo la funcion resta con dos parametros
    return(num1-num2);//retorno su resultado
}

function multiplica(num1,num2){//creo la funcion multiplica con dos parametros
    return(num1*num2);//retorno su resultado
}

function divide(num1,num2){//creo la funcion divide con dos parametros
    return(num1/num2);//retorno su resultado
}
