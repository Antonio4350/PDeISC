function funcion(n){
    switch(n){
        case 0:
            document.body.innerHTML+="<h1>Titulo</h1>"
        break;
        case 1:
            document.body.innerHTML+="<p>Texto</p>"
        break;
        case 2:
        document.body.innerHTML += '<img src="https://francescricart.com/wp-content/uploads/2019/03/como-crear-pagina-en-construccion.jpg">';
        break;
        case 3:
            document.body.innerHTML+="<button>Boton</button>"
        break;
    }
}